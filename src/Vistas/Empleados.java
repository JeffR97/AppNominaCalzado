    //------------------------------------------------------------------------------------------------------------------------------- 
    void consultar() {
        String sql = "SELECT * FROM empleados";
        try {
            conect = con1.getConnection();
            st = (Statement) conect.createStatement();
            rs = (ResultSet) st.executeQuery(sql);

            Object[] datos = new Object[3];
            modelo = (DefaultTableModel) TableEmp.getModel();

            while (rs.next()) {
                datos[0] = rs.getInt("idEmple");
                datos[1] = rs.getString("nomEmple");
                datos[2] = rs.getString("cargo");

                modelo.addRow(datos);
            }
        } catch (Exception e) {
        }
    }

    //------------------------------------------------------------------------------------------------------------------------------- 
    void eliminar() {
        int fila = TableEmp.getSelectedRow();

        try {
            if (fila < 0) {
                JOptionPane.showMessageDialog(null, "Empleado no seleccionado");
            } else {
                String sql = "DELETE FROM empleados WHERE idEmple =" + idc;
//              DELETE FROM `usuario` WHERE 0
                conect = con1.getConnection();
                st = (Statement) conect.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Se EliminÃ³ al Empleado de la BD");
                limpiar();
            }

        } catch (Exception e) {

        }
    }

    //-------------------------------------------------------------------------------------------------------------------------------     
    void limpiar() {
        for (int i = 0; i <= TableEmp.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

